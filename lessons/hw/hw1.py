class Person:
    # Конструктор класса, инициализирует атрибуты объекта
    def __init__(self, name, age, city):
        self.name = name  # Имя человека
        self.age = age    # Возраст человека
        self.city = city  # Город проживания человека

    # Метод для представления человека
    def introduce(self):
        # Выводит информацию о человеке в формате строки
        print(f"Привет, меня зовут {self.name}, мне {self.age} лет, я живу в {self.city}.")

    # Метод для проверки, является ли человек совершеннолетним
    def is_adult(self):
        # Возвращает True, если возраст 18 или больше, иначе False
        return self.age >= 18

    # Метод для определения, как объект будет представляться в строковом виде
    def __str__(self):
        # Возвращает строку с информацией о человеке в заданном формате
        return f"Имя: {self.name}, Возраст: {self.age}, Город: {self.city}"

#  Создание экземпляра класса Person и вызов метода introduce
person1 = Person("Алексей", 30, "Москва")  # Создание объекта person1 с заданными параметрами
person1.introduce()  # Вызов метода introduce для вывода информации о person1

#  Создание нескольких экземпляров класса Person
person2 = Person("Мария", 17, "Санкт-Петербург")  # Создание объекта person2
person3 = Person("Иван", 20, "Казань")  # Создание объекта person3

# Проверка, является ли каждый из людей совершеннолетним
for person in [person1, person2, person3]:  # Проходим по списку объектов
    print(f"{person.name} совершеннолетний: {person.is_adult()}")  # Выводим результат проверки is_adult для каждого

# Задание 3: Создание экземпляра класса Person и использование метода __str__
person4 = Person("Елена", 25, "Новосибирск")  # Создание объекта person4
print(person4)  # Печать объекта person4, что вызывает метод __str__ и выводит информацию