# Импортируем библиотеку requests для отправки HTTP-запросов
import requests

# Задаем название города для получения информации о погоде
city = "Bishkek"  # Название города

# Создаем URL-адрес для запроса к API wttr.in
# ?format=%C+%t указывает, что нужно вернуть краткую информацию о погоде:
# %C - описание состояния погоды (например, "Clear" или "Rain")
# %t - температура в градусах Цельсия
url = f"http://wttr.in/{city}?format=%C+%t"

# Отправляем GET-запрос к серверу по созданному URL
response = requests.get(url)

# Проверяем, успешен ли запрос (код состояния 200 - успешный запрос)
if response.status_code == 200:
    # Если запрос успешен, выводим результат (текущая погода и температура)
    print(f"Погода в {city}: {response.text}")
else:
    # Если запрос не успешен, выводим сообщение об ошибке с кодом состояния
    print("Ошибка при получении данных о погоде:", response.status_code)
